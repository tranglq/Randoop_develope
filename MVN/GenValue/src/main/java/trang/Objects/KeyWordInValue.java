public class KeyWordInValue {
	private String start = "START CLASSLITERALS";
	private String endd = "END CLASSLITERALS";
	private String classkey = "CLASSNAME";
	private String lit = "LITERALS";
	
	public String getStart() {
		return this.start;
	}
	
	public String getEnd() {
		return this.endd;
	}
	
	public String getClassKey() {
		return this.classkey;
	}
	
	public String getLit() {
		return this.lit;
	}
}
